QUERY_TEMPERATURE = """
    SELECT
      year,
      mo as month,
      da as day,
      MAX(max) as max_temp,
      MIN(min) as min_temp,
      AVG(temp) as avg_temp,
      name
    FROM
      `bigquery-public-data.noaa_gsod.gsod%(year)s` a
    JOIN
      `bigquery-public-data.noaa_gsod.stations` b
    ON
      a.stn=b.usaf
      AND a.wban=b.wban
    WHERE
      max < 1000
      AND country = 'FR'
      AND name = '%(name)s'
    GROUP BY
      year, month, day, name
    ORDER BY
      year, month, day, name
"""

